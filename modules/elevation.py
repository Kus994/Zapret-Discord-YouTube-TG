"""
modules/elevation.py
---------------------
Проверка и запрос прав администратора (UAC) при старте приложения.

Используется в main.py: если процесс запущен без прав администратора,
скрипт пытается перезапустить себя через ShellExecuteW с verb='runas',
что вызывает стандартный диалог UAC Windows.

ВАЖНО (фикс): раньше relaunch_as_admin() вызывал sys.exit(0) сразу
после ShellExecuteW, не проверяя, действительно ли перезапуск удался.
Если пользователь отклонял диалог UAC (нажимал «Нет») или ShellExecuteW
не мог найти/запустить pythonw.exe — исходный процесс всё равно
завершался с кодом 0, а новый процесс не появлялся. Итог: ни одного
окна на экране, ни единой ошибки в логе — именно то, что выглядит как
«ничего не происходит». Теперь relaunch_as_admin() проверяет код
возврата ShellExecuteW и завершает процесс только при реальном успехе.
"""

import ctypes
import sys
from pathlib import Path

# ShellExecuteW возвращает HINSTANCE. Согласно документации Microsoft,
# любое значение <= 32 — это код ошибки (привязанный к старым кодам
# ошибок WinExec), а не настоящий HINSTANCE. В частности:
#   0  (ERROR_OUT_OF_MEMORY-подобная ошибка / нехватка ресурсов)
#   2  (ERROR_FILE_NOT_FOUND — не найден исполняемый файл)
#   5  (ERROR_ACCESS_DENIED — пользователь отклонил запрос UAC)
#   8  (ERROR_NOT_ENOUGH_MEMORY)
#   26, 27, 28, 29, 30, 31 (разные варианты "не удалось выполнить")
SHELLEXECUTE_ERROR_THRESHOLD = 32

# Человекочитаемые расшифровки самых частых кодов — пригодится в логе,
# чтобы сразу было понятно, что произошло, без поиска кода в интернете.
_SHELLEXECUTE_ERRORS = {
    0: "недостаточно ресурсов системы",
    2: "исполняемый файл не найден",
    3: "путь не найден",
    5: "доступ запрещён — пользователь отклонил запрос UAC, "
       "либо политика безопасности блокирует повышение прав",
    8: "недостаточно памяти",
    26: "ошибка совместного доступа к файлу",
    27: "имя файла повреждено или неполное",
    28: "превышен таймаут операции",
    29: "ошибка взаимодействия с DDE",
    30: "DDE-канал занят другой задачей",
    31: "нет ассоциированного приложения для этого типа файла, "
        "либо файл не найден",
}


def is_admin() -> bool:
    """Возвращает True, если текущий процесс имеет права администратора."""
    try:
        return ctypes.windll.shell32.IsUserAnAdmin() != 0
    except Exception:
        return False


def _windowless_python() -> str:
    """
    Возвращает путь к pythonw.exe (без консольного окна), если он есть
    рядом с текущим интерпретатором. Если запуск уже идёт через pythonw
    (sys.executable уже оканчивается на pythonw.exe) — возвращает его же.
    Если pythonw.exe не найден — откатывается на обычный sys.executable,
    чтобы приложение всё равно запустилось (просто с видимой консолью).
    """
    current = Path(sys.executable)
    if current.name.lower() == "pythonw.exe":
        return str(current)

    candidate = current.with_name("pythonw.exe")
    if candidate.exists():
        return str(candidate)

    return str(current)


def relaunch_as_admin() -> "tuple[bool, str]":
    """
    Пытается перезапустить текущий скрипт с правами администратора
    через UAC. Возвращает (success, message):

      success=True  — новый повышенный процесс реально запущен;
                       вызывающая сторона должна завершить текущий
                       процесс (sys.exit), чтобы не было двух копий.
      success=False — перезапуск не удался (UAC отклонён, файл не
                       найден и т.п.); вызывающая сторона должна
                       продолжить работу в текущем, неэлевированном
                       процессе — лучше запустить без прав админа,
                       чем не запуститься вообще.

    В отличие от предыдущей версии, здесь НЕ вызывается sys.exit —
    это решение оставлено main.py, чтобы было видно, что произошло,
    и чтобы можно было залогировать причину перед выходом.
    """
    # ВАЖНО (третий фикс той же цепочки проблем): раньше здесь
    # использовался pythonw.exe (без консоли) — из соображений
    # эстетики, чтобы не мигало лишнее окно. Но если повышенный
    # процесс падает уже ПОСЛЕ старта (например, из-за проблемы,
    # которую не предугадать заранее — отсутствующая зависимость,
    # блокировка антивирусом, что угодно), у pythonw.exe физически
    # нет консоли, чтобы показать ошибку: ни окна, ни traceback'а,
    # только тишина. Это и произошло — UAC проходил успешно (код 42),
    # но само окно ни разу так и не появилось, а причину было
    # невозможно даже увидеть. Пока стабильность важнее эстетики:
    # используем ту же консольную python.exe, что и текущий процесс,
    # чтобы при следующем сбое (если он есть) traceback был виден на
    # экране, а не терялся безвозвратно.
    exe = sys.executable

    # ВАЖНО (второй фикс той же проблемы): sys.argv[0] может быть
    # ОТНОСИТЕЛЬНЫМ путём — например, run.bat запускает `py main.py`
    # из папки со скриптом. ShellExecuteW с verb="runas" проходит через
    # UAC-повышение, и новый процесс нередко стартует с рабочей
    # директорией C:\Windows\System32 (стандартная особенность Windows
    # при повышении прав), а НЕ с рабочей директорией родителя. Из-за
    # этого повышенный процесс не мог найти "main.py" по
    # относительному пути и падал ещё до того, как успевал написать
    # хоть строку в лог. Исправление: путь к скрипту приводится к
    # абсолютному, а рабочая директория передаётся явно пятым
    # параметром ShellExecuteW.
    script_path = Path(sys.argv[0]).resolve()
    params = " ".join(
        '"{}"'.format(a) for a in [str(script_path)] + sys.argv[1:]
    )
    work_dir = str(script_path.parent)

    try:
        result = ctypes.windll.shell32.ShellExecuteW(
            None, "runas", exe, params, work_dir, 1  # SW_SHOWNORMAL=1
        )
    except Exception as exc:
        return False, "ShellExecuteW бросил исключение: {}".format(exc)

    code = int(result)
    if code > SHELLEXECUTE_ERROR_THRESHOLD:
        return True, "Повышенный процесс запущен (код {})".format(code)

    reason = _SHELLEXECUTE_ERRORS.get(code, "код ошибки {}".format(code))
    return False, "Не удалось запустить с правами администратора: {}".format(reason)


def ensure_admin():
    """Оставлено для обратной совместимости — не используется напрямую
    в текущей версии main.py (там своя обработка результата через
    relaunch_as_admin), но может быть полезно для внешнего кода."""
    if not is_admin():
        relaunch_as_admin()
